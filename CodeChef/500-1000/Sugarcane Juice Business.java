import java.util.*;




class Codechef
{

    public static
    void
    main(String[] args)
    {
        Scanner read = new Scanner(System.in);
        int T = read.nextInt();


        for
        (
            int h = 0
            ; h < T
            ; h ++
        )
        {
            System.out.println( read.nextInt() * 15 );
        }
        
        
    }

}
